package org.wickedsource.docxstamper.context;

public class NameContext {

    private String name;

    public NameContext(String name) {
        this.name = name;
    }

    public NameContext() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
